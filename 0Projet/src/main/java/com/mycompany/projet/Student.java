    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        // Get the student information from the input fields
        String studentNom = studentnom.getText();
        String studentPrenom = studentprenom.getText();
        String studentDateOfBirth = studentdatebirdth.getText();
        String studentId = studentnumber.getText();
        String courseCode = coursecode.getText();
        String finalNote = studentnote.getText();

        // Create a new Etudient object with the provided information
        Etudient newStudent = new Etudient(studentNom, studentPrenom, studentDateOfBirth, studentId, finalNote);

        // Check if the student with the given ID already exists in any course
        boolean studentExists = false;
        for (int i = 0; i < C.size(); i++) {
            Course course = C.get(i);
            for (int j = 0; j < course.getStudents().size(); j++) {
                Etudient student = course.getStudents().get(j);
                if (student.getEtudiantId().equals(studentId) && course.getCourseNumber().equals(courseCode)) {
                    // If the student already exists, set the flag to true and break the loop
                    studentExists = true;
                    break;
                }
            }
        }
        
        if (studentExists) {
            // If the student already exists, show a message dialog
            JOptionPane.showMessageDialog(this, "Student with the given ID already exists!");
        } else {
            // If the student does not exist, check if the course with the given code exists
            boolean courseExists = false;
            int courseIndex = -1;
            for (int i = 0; i < C.size(); i++) {
                if (C.get(i).getCourseNumber().equals(courseCode)) {
                    // If the course exists, set the flag to true and remember the course index
                    courseExists = true;
                    courseIndex = i;
                    break;
                }
            }
            
            if (!courseExists) {
                // If the course does not exist, show a message dialog
                JOptionPane.showMessageDialog(this, "Course with the given code does not exist. Please add the course first.");
            } else {
                // If the course exists, add the new student to the course's list of students
                C.get(courseIndex).getStudents().add(newStudent);
                // Show a success message dialog
                JOptionPane.showMessageDialog(this, "Student added successfully!");
            }
        }
        

    }//GEN-LAST:event_jButton19ActionPerformed

