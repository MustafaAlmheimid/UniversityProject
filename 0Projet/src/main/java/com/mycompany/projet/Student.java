// Function to edit a student's information
    private void EditEtudient(String studentId) {
        // Get the updated student information from the input fields
        String updatedNom = studentnom.getText();
        String updatedPrenom = studentprenom.getText();
        String updatedDateOfBirth = studentdatebirdth.getText();
        String updatedFinalNote = studentnote.getText();

        // Find the course and student that matches the provided student ID
        Course courseToUpdate = null;
        Etudient studentToUpdate = null;
        
        for (int i = 0; i < C.size(); i++) {
            Course course = C.get(i);
            for (int j = 0; j < course.getStudents().size(); j++) {
                Etudient student = course.getStudents().get(j);
                if (student.getEtudiantId().equals(studentId)) {
                    courseToUpdate = course;
                    studentToUpdate = student;
                    break;
                }
            }
            if (courseToUpdate != null && studentToUpdate != null) {
                break;
            }
        }

        // If the student with the given ID is found, update their information
        if (courseToUpdate != null && studentToUpdate != null) {
            studentToUpdate.setEtudiantNom(updatedNom);
            studentToUpdate.setEtudiantPrenom(updatedPrenom);
            studentToUpdate.setEtudiantDateBirdth(updatedDateOfBirth);
            studentToUpdate.setFinalNote(updatedFinalNote);

            // Show a success message dialog
            JOptionPane.showMessageDialog(this, "Student information updated successfully!");
        } else {
            // If the student with the given ID is not found, show an error message dialog
            JOptionPane.showMessageDialog(this, "Student with the given ID not found!");
        }
    }
    

